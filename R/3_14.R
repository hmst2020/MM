T<-function(x1,x2,x3){           # 自訂一函式用以計算線性轉換T
  return (c(2*x1+x2-x3, -x1+3*x2-2*x3, 3*x2+4*x3))
}
(Te1<-T(1,0,0))                 # 標準基底e1線性轉換
(Te2<-T(0,1,0))                 # 標準基底e2線性轉換
(Te3<-T(0,0,1))                 # 標準基底e3線性轉換
(A<-cbind(Te1,Te2,Te3))         # 轉換矩陣

x<-c(1,2,3)                     # 隨意設一向量x
A%*%x                           # Ax計算線性轉換的像
T(1,2,3)                        # T函式帶入引數計算線性轉換的像
