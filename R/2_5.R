A <- matrix(              # 建立產出與消費矩陣
  c(0.278,0.125,0.333,
    0.111,0.188,0.167,
    0.167,0.125,0.167),
  nrow = 3, 
  byrow=TRUE,
  dimnames=list(     # 以list物件賦予列名、行名
    c('R','S','T'),
    c('R','S','T')
  ))
print(A)                 # 印出產出與消費矩陣
(I <- diag(3))           # 定義與上述A同為3x3的單位矩陣
(D <- matrix(            # 定義三年後的客戶需求矩陣
  c(60,110,60),
  ncol = 1, 
  byrow=TRUE))
solve(I -A)%*% D    # 代入公式，求滿足預估的R、S、T產出值
