########### 是否為線性獨立 ###########
a<-cbind(              # 向量集之矩陣
  c(1,1,1),            # x1向量
  c(1,2,3),            # x2向量
  c(0,1,0))            # x3向量
b<-c(0,0,0)                # 線性獨立的組合為0向量
det(a)                     # <>0 表示有唯一解
(x<-solve(a,b))            # 此為唯一解
